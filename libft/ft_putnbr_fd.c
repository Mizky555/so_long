#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	i;
	char	iii;

	i = (long)n;
	if (i < 0)
	{
		write(fd, "-", 1);
		i *= -1;
	}
	if (i >= 10)
	{
		ft_putnbr_fd(i / 10, fd);
	}
	iii = (i % 10) + '0';
	write(fd, &iii, 1);
}
